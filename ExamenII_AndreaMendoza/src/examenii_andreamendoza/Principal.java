    private void LogInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LogInMouseClicked
        String un = jt_userlogin.getText();
        String pw = pw_pwlogin.getText();
        System.out.println(Users);
        for (Usuarios U : Users) {
            if (U.getUN().equalsIgnoreCase(un) && U.getPW().equals(pw)) {
                USER = U;
                A = (ATM) Sucursal.getSelectedItem();
                JOptionPane.showMessageDialog(this, "¡Bievenido a nuestra sucursal ATM, " + USER.getPNombre() + " " + USER.getPApellido());
                if (USER instanceof Cliente && (!(Sucursal.getSelectedItem().toString().equals("Sucursal Principal")))) {
                    Whatever.pack();
                    Whatever.setLocationRelativeTo(null);
                    Whatever.setVisible(true);
                    this.setVisible(false);
                } else if (USER instanceof Cliente && Sucursal.getSelectedItem().toString().equals("Sucursal Principal")) {
                    Principal.pack();
                    Principal.setLocationRelativeTo(null);
                    Principal.setVisible(true);
                    this.setVisible(false);
                } else if (USER instanceof Mantenimiento) {
                    Maintenance.pack();
                    Maintenance.setLocationRelativeTo(null);
                    Maintenance.setVisible(true);
                    this.setVisible(false);
                    DefaultComboBoxModel m = (DefaultComboBoxModel) Cuentas2.getModel();
                    for (ATM a : ((Mantenimiento) USER).getAsignados()) {
                        m.addElement(a);
                    }
                }
                break;
            } else if (U.getUN().equalsIgnoreCase(un) && (!U.getPW().equals(pw))) {
                pw_pwlogin.setBackground(Color.pink);
                JOptionPane.showMessageDialog(this, "Contraseña incorrecta.\nInténtelo de nuevo.");
                pw_pwlogin.setText("");
                break;
            } else if (Users.indexOf(U) == Users.size() - 1 && (!U.getUN().equals(un))) {
                JOptionPane.showMessageDialog(this, "El usuario ingresado no existe.\nInténtelo de nuevo con otro usuario o contacte el administrador del equipo.");
                jt_userlogin.setText("");
                pw_pwlogin.setText("");
            }
        }

    }//GEN-LAST:event_LogInMouseClicked


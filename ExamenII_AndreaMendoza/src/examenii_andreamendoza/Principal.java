    private void Do1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Do1MouseClicked
        double SaldoA = ((Cuenta) CuentaP.getSelectedItem()).getSaldo();
        int Ret = Integer.parseInt(Total1.getText());
        if (Ret > SaldoA) {
            JOptionPane.showMessageDialog(null, "No tiene dinero suficiente para realizar el retiro.");
        } else {
            ((Cuenta) CuentaP.getSelectedItem()).setSaldo(SaldoA - Ret);
            JOptionPane.showMessageDialog(null, "¡Retiro realizado exitósamente!\n Su saldo actual es de HNL." + ((Cuenta) CuentaP.getSelectedItem()).getSaldo());
            ((Cliente) USER).getLog().add(new Logs(USER.getUN(), "Retiro de HNL." + Ret, new Date()));
            A.setSaldoTotal(A.getSaldoTotal() - Ret);
        }
        Retiros.setVisible(false);
        CuentaP.setSelectedIndex(0);
        Total1.setText("");
        C100.setValue(0);
        C500.setValue(0);
        BinaryManager BM = new BinaryManager("./User Information.aj");
        BM.cargarArchivo();
        for (Object user1 : BM.getUsers()) {
            if (((Cliente) user1).getUN().equals(USER.getUN())) {
                for (Cuenta c : ((Cliente) user1).getCuentas()) {
                    if (c.equals(((Cuenta) CuentaP.getSelectedItem()))) {
                        c.setSaldo(SaldoA - Ret);
                    }
                }

            }
        }
        BM.escribirArchivo();
        ATMManager AM = new ATMManager("./ATM Information.aj");
        AM.cargarArchivo();
        for (Object user1 : AM.getATMS()) {
            if (((ATM) user1).equals(A)) {
                ((ATM) user1).setSaldoTotal(A.getSaldoTotal() - Ret);
            }

        }
        AM.escribirArchivo();
    }//GEN-LAST:event_Do1MouseClicked

